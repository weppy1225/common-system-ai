async function findStatCells(sheet) {
  // Search for WEB/PDA/I/F labels in Sheet1
  const result = { webRow: -1, pdaRow: -1, ifRow: -1, totalRow: -1, startCol: -1 };
  try {
    const usedRange = sheet.usedRange();
    if (!usedRange) return result;
    const maxRow = usedRange.endCell().rowNumber();
    const maxCol = usedRange.endCell().columnNumber();

    for (let r = 1; r <= maxRow; r++) {
      for (let c = 1; c <= maxCol; c++) {
        const val = sheet.row(r).cell(c).value();
        if (val === 'WEB') { result.webRow = r; result.startCol = c; }
        if (val === 'PDA') { result.pdaRow = r; }
        if (val === 'I/F') { result.ifRow = r; }
        if (val === '합계' || val === '합 계') { result.totalRow = r; }
      }
    }
  } catch (e) {
    console.warn('Sheet1 scan error:', e.message);
  }
  return result;
}

